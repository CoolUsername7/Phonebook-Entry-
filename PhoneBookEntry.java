public class PhoneBookEntry
{
    private String name;
    private String number;

public PhoneBookEntry()
{

}

public String getName()
{
    return name;
}

public String getNumber()
{
    return number;
}

public void setName(String s)
{
    name = s;
}

public void setNumber(String s)
{
    number = s;
}
}